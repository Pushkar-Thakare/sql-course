-- UNION

-- Displays results of two queries with same columns combined

-- UNION vertically combines the results of two compatible queries.
-- Both queries must return the same number of columns with compatible types.
-- UNION removes duplicate rows.
-- UNION ALL keeps duplicates.

-- Syntax EX: 

-- SELECT * FROM finance_report_Q1
-- UNION
-- SELECT * FROM finance_report_Q2

-- Hence it displays financial report of Q1 and Q2 combined in one query.
